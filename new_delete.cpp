#include <iostream>

typedef struct Animal {
	char name[30];  // 이름
	int age;        // 나이

	int health;  // 체력
	int food;    // 배부른 정도
	int clean;   // 깨끗한 정도
} Animal;

void create_animal(Animal *animal) {
	std::cout << "동물의 이름? ";
	std::cin >> animal->name;

	std::cout << "동물의 나이? ";
	std::cin >> animal->age;

	animal->health = 100;
	animal->food = 100;
	animal->clean = 100;
}
void play(Animal *animal) {
	animal->health += 10;
	animal->food -= 20;
	animal->clean -= 30;
}
void one_day_pass(Animal *animal) {
	// 하루가 지나면
	animal->health -= 10;
	animal->food -= 30;
	animal->clean -= 20;
}
void show_stat(Animal *animal) {
	std::cout << animal->name << "의 상태" << std::endl;
	std::cout << "체력    : " << animal->health << std::endl;
	std::cout << "배부름 : " << animal->food << std::endl;
	std::cout << "청결    : " << animal->clean << std::endl;
}


int main() {
	int* p = new int;  //int 크기의 공간을 할당 -> 그 주소값을 p에 (malloc과 비슷)
	*p = 10;
	std::cout << *p << std::endl; //10
	delete p; //할당 공간 해제



	// new 로 배열 할당하기 
	int arr_size;
	std::cout << "array size : ";
	std::cin >> arr_size;
	int *list = new int[arr_size];
	for (int i = 0; i < arr_size; i++) {
			std::cin >> list[i];
		}
		for (int i = 0; i < arr_size; i++) {
			std::cout << i << "th element of list : " << list[i] << std::endl;
		}
		delete[] list;





	Animal *alist[10];
	int animal_num = 0;

	for (;;) {
		std::cout << "1. 동물 추가" << std::endl;
		std::cout << "2. 동물이랑 놀기 " << std::endl;
		std::cout << "3. 동물 상태" << std::endl;

	    int input;
		std::cin >> input;

		switch (input) {
			int play_with;
			case 1:
				alist[animal_num] = new Animal;
				create_animal(alist[animal_num]);

				animal_num++;
				break;
			case 2:
				std::cout << "누구랑 놀게? (동물 번호 입력) : ";
				std::cin >> play_with;

				if (play_with < animal_num) play(alist[play_with]);

				break;

			case 3:
				std::cout << "누구껄 보게? (동물 번호 입력) : ";
				std::cin >> play_with;
				if (play_with < animal_num) show_stat(alist[play_with]);
				break;
			}

			for (int i = 0; i != animal_num; i++) {
				one_day_pass(alist[i]);
			}
		}
		for (int i = 0; i != animal_num; i++) {
			delete alist[i];
		}
	return 0;
}